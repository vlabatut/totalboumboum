	/**
	 * termine proprement cette simulation (une fois que l'IA n'en a plus besoin).
	 */
	public void finish()
	{	// matrix
		for(int line=0;line<height;line++)
			for(int col=0;col<width;col++)
				matrix[line][col].finish();
		matrix = null;
		
		// sprites
		blockList.clear();
		bombList.clear();
		fireList.clear();
		floorList.clear();
		heroList.clear();
		itemList.clear();
		ownHero = null;
	}
